package Model;

public class JangDTO {

	private int no;
	private String img;
	private String name;
	private int cnt;
	private int price;
	private int chk_n;
	
	public int getChk_n() {
		return chk_n;
	}
	public void setChk_n(int chk_n) {
		this.chk_n = chk_n;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
